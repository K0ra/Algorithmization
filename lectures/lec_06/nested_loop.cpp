#include <iostream>

int main() {

    // 1 * 1    1 * 2   1 * 3   1 * 4 ... 1 * 10
    // 2 * 1    2 * 2   2 * 3   2 * 4 ... 2 * 10
    // 3 * 1    3 * 2   3 * 3   3 * 4 ... 3 * 10
    // ...
    // 10 * 1   10 * 2   10 * 3   10 * 4 ... 10 * 10

    for (int i = 1; i <= 10; ++i) {
        for (int j = 1; j <= 10; ++j) {
            std::cout << i * j << '\t';
        }
        std::cout << '\n';
    }
}